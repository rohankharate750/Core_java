class switch1
{
    public static void main (String [] args)
    {
        int n = 2;

        switch(n) {
            case 1:
            System.out.println("Monday");
            break;

            case 2:
            System.out.println("tuesday");
            break;

            case 3:
            System.out.println("Wednesday");
            break;

            case 4:
            System.out.println("Thurday");
            break;

            case 5:
            System.out.println("friday");
            break;

        }
    }
}